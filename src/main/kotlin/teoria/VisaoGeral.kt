Visão geral programa

Programa: sequência de instruções executadas uma após a outra de maneira previsível. O Fluxo sequencial é o mais comum e se trata quando as instruções são executdas na ordem que são escritas,
ou seja, de cima pra baixo.

Instrução (Statement): um único programa a ser executado, por exemplo: imprimir um texto.

Expressão: um pedaço de código que produz um único valor, por exemplo -> 5 + 5

Bloco: grupo de zero ou mais instruções entre chaves {...}

Palavra-chave: palavra que tem significado especial na linguagem de programação.



Famoso Hello World:



fun main() {
	 println("Hello, World!")
}

A palavra-chave "fun" define uma função que contém um pedaço de código a ser executado.
Essa função em específico tem um nome especial MAIN. que indica o ponto de entrada de um programa Kotlin.
A função tem um corpo que está incluso nas chaves {...}


"Printando" "Hello World": o corpo da função consiste em instruções que dedfinem o que o programa vai fazer, no nosso caso, 'printar' a string "Hellow World!", usando a instrução:
println("Hello World").

O que importa que entedemos aqui: invocamos uma função especial chamada println para apresentar a string seguida por uma linha na tela.
 */