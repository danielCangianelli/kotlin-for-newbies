/*
 Literais em kotlin
 Independente da complexidade, os programas executam operações em números, strings e outros valores. Esses valores são
 chamados de literais, ou seja, no sentido mais básico do símbolo.
 Para nós: Números inteiros, caracteres e strings


##INT##
Exemplos de números inteiros:

1
2
10
100

1000000

Se um valor tiver muitos dígitos, podemos adicionar underscores para dividir os blocos e tornar mais legível.

1_000_000 vs 1000000

Tomar cuidado com aberrações:
1___000_000, pois o número de undescores consecutivos não é limitado!

Não podemos iniciar ou encerrar um Int com underscore.

_1 ou 3_


##Char##

Um único caractere pode representar um dígito, uma letra ou outro símbolo, para escrever eles usamos aspas simpes:
(Podendo ser qualquer letra do alfabeto, dígitos de 0 a 9, espaços em brancos e alguns símbolos, por exemplo '$'
'A'
'x'
'0'
'5'

##String##

Trata-se de uma sequência qualquer de caracteres.
Usamos aspas duplas pra representá-las:

"Kotlin"
"Swift sucks!"
"e-mail@gmail.com"
"A"

